#include "Reg_SP37.h"
#include "SP37_ROMLibrary.h"
#include <uart.h>
#include <stdio.h>
#include <ctype.h>

void MAIN(void)
{
	Uart_Init();
	
	while(1){

		WDRES = 1;		//喂看门狗
		printf("%s\r\n","SP370 Printf Test...");
		printf("十进制:%d\r\n",10000);
		printf("字符:%c\r\n",'A');
		//打印16进制需要将数据强制为int
		printf("十六进制:0x%X\r\n",(int)0xAA);

		//延时1S
		Wait100usMultiples(10000);
	}
}




