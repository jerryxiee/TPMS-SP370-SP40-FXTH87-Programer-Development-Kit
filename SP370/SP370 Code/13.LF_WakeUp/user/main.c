#include "Reg_SP37.h"
#include "SP37_ROMLibrary.h"
#include <uart.h>
#include <SP37.h>
#include <RF.h>
#include <LF.h>


/* ***************************************************************************** */
/* 此例程为LF载波侦测模式,也就是只要感应到电感有电压(超过最低阈值),就会唤醒      */
/* LF 相关文档请参考"SP37_LF_v1.0.pdf","SP37_Datasheet_V1.0.pdf"                 */
/* 文档位于"SP37 相关资料"或者"SP37 芯片数据手册"                                */
/* ***************************************************************************** */


void MAIN(void)
{
	unsigned char store_wuf;

	Uart_Init();

	

	store_wuf = WUF;						//记录唤醒源
	printf("Power On,WUF = %02X\r\n",(int)store_wuf);	//每次PowerOn时printf WUF的值

	if(store_wuf == 0x00){			//第一次上电的时候,进行配置
															//第一次上电的时候WUF = 0x00
		LF_Configure_Carrier_Detect();
	}
	if(store_wuf & 0x20){
		printf("LF Wake Up...\r\n");
	}

	Powerdown();
}

